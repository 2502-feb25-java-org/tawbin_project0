package com.revature.model;

public class ReimbType {
	
	private int TypeID;
	private String Type;
	public ReimbType(int typeID, String type) {
		super();
		TypeID = typeID;
		Type = type;
	}
	public int getTypeID() {
		return TypeID;
	}
	public void setTypeID(int typeID) {
		TypeID = typeID;
	}
	public String getType() {
		return Type;
	}
	public void setType(String type) {
		Type = type;
	}
	
	
	
	
	
	

}
